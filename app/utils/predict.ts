// Build word trie: for each word, index by prefix of length 1-2
function buildWordTrie(): void {
  wordTrie.clear();
  for (const [word, freq] of WORD_LIST) {
    if (!word || word.length < 2) continue;
    // Index by first character
    const key1 = word[0]!;
    if (!wordTrie.has(key1)) wordTrie.set(key1, []);
    wordTrie.get(key1)!.push({ word, freq });
    // Also index by first 2 chars (for chain prediction)
    if (word.length >= 3) {
      const key2 = word.slice(0, 2);
      if (!wordTrie.has(key2)) wordTrie.set(key2, []);
      wordTrie.get(key2)!.push({ word, freq });
    }
  }
  // Sort each bucket by frequency descending
  for (const [, entries] of wordTrie) {
    entries.sort((a, b) => b.freq - a.freq);
  }
}
buildWordTrie();

