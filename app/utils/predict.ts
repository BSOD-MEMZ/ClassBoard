export interface PredictionResult {
  /** The predicted text (single char or multi-char word) */
  text: string;
  /** Whether this is a multi-character word prediction */
  isWord: boolean;
}

/**
 * Get single-character 联想 candidates for the given character.
 */
export function getCharPredictions(char: string, limit = 8): string[] {
  const followers = bigramMap.get(char);
  if (!followers || !followers.length) return [];
  return followers.slice(0, limit);
}

/**
 * Get multi-character word predictions starting with the given prefix.
 */
export function getWordPredictions(prefix: string, limit = 6): string[] {
  const entries = wordTrie.get(prefix);
  if (!entries || !entries.length) return [];
  return entries.slice(0, limit).map(e => e.word);
}

/**
 * Combined prediction: single chars + multi-char words.
 * Words are shown first (more useful), then single chars.
 */
export function getPredictions(char: string, limit = 12): PredictionResult[] {
  const results: PredictionResult[] = [];

  // Multi-char word predictions (prioritize)
  const words = getWordPredictions(char, 6);
  for (const w of words) {
    results.push({ text: w, isWord: true });
  }

  // Single-char predictions (fill remaining slots)
  const chars = getCharPredictions(char, limit - results.length);
  for (const c of chars) {
    // Don't duplicate if already covered by a word
    if (!results.some(r => r.text === c)) {
      results.push({ text: c, isWord: false });
    }
  }

  return results.slice(0, limit);
}

/**
 * Chain prediction: given the last 2 committed characters, predict next.
 */
export function getChainPredictions(lastTwo: string, limit = 6): PredictionResult[] {
  if (lastTwo.length < 2) return [];
  const results: PredictionResult[] = [];
  const words = getWordPredictions(lastTwo, limit);
  for (const w of words) {
    // Only include words that extend beyond the prefix
    if (w.length > lastTwo.length) {
      results.push({ text: w, isWord: true });
    }
  }
  return results.slice(0, limit);
}

export function hasPredictions(char: string): boolean {
  return (bigramMap.has(char) && bigramMap.get(char)!.length > 0)
      || (wordTrie.has(char) && wordTrie.get(char)!.length > 0);
}
